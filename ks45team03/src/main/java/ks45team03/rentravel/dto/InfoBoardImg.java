package ks45team03.rentravel.dto;

public class InfoBoardImg {
	private String infoBoardImgCode;
	private String infoBoardCode;
	private String infoImg;
	private String imgRegTime;
	
	public String getInfoBoardImgCode() {
		return infoBoardImgCode;
	}
	public void setInfoBoardImgCode(String infoBoardImgCode) {
		this.infoBoardImgCode = infoBoardImgCode;
	}
	public String getInfoBoardCode() {
		return infoBoardCode;
	}
	public void setInfoBoardCode(String infoBoardCode) {
		this.infoBoardCode = infoBoardCode;
	}
	public String getInfoImg() {
		return infoImg;
	}
	public void setInfoImg(String infoImg) {
		this.infoImg = infoImg;
	}
	public String getImgRegTime() {
		return imgRegTime;
	}
	public void setImgRegTime(String imgRegTime) {
		this.imgRegTime = imgRegTime;
	}
}
