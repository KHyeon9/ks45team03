package ks45team03.rentravel.dto;

public class InfoBoardComment {
	private String infoBoardCommentCode;
	private String infoBoardCode;
	private String userId;
	private String infoBoardCommentContent;
	private String infoBoardCommentRegTime;
	
	private String userNickName;
	
	public String getInfoBoardCommentCode() {
		return infoBoardCommentCode;
	}
	public void setInfoBoardCommentCode(String infoBoardCommentCode) {
		this.infoBoardCommentCode = infoBoardCommentCode;
	}
	public String getInfoBoardCode() {
		return infoBoardCode;
	}
	public void setInfoBoardCode(String infoBoardCode) {
		this.infoBoardCode = infoBoardCode;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getInfoBoardCommentContent() {
		return infoBoardCommentContent;
	}
	public void setInfoBoardCommentContent(String infoBoardCommentContent) {
		this.infoBoardCommentContent = infoBoardCommentContent;
	}
	public String getInfoBoardCommentRegTime() {
		return infoBoardCommentRegTime;
	}
	public void setInfoBoardCommentRegTime(String infoBoardCommentRegTime) {
		this.infoBoardCommentRegTime = infoBoardCommentRegTime;
	}
	public String getUserNickName() {
		return userNickName;
	}
	public void setUserNickName(String userNickName) {
		this.userNickName = userNickName;
	}
	
	
}
